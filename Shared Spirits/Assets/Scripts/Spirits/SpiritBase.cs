    public static float GetEffectiveness(SpiritType attackType, SpiritType defendType)
    {
        if (attackType == SpiritType.None || defendType == SpiritType.None)
            return 1f;

        int row = (int)attackType - 1;
        int col = (int)defendType - 1;

        return chart[row][col];
    }
